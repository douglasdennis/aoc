function ParseString (s)
    local digits = {}
    for i=0,9 do digits["" .. i] = true end
    local number_words = {
        ["zero"] = 0,
        ["one"] = 1,
        ["two"] = 2,
        ["three"] = 3,
        ["four"] = 4,
        ["five"] = 5,
        ["six"] = 6,
        ["seven"] = 7,
        ["eight"] = 8,
        ["nine"] = 9,
    }
    local candidate_string = ""
    for i=1,string.len(s) do
        if digits[string.sub(s, i, i)] then
            FirstDigit = string.sub(s, i, i)
            FirstDigitPosition = i
            break
        end
        if number_words[candidate_string .. string.sub(s, i, i)] ~= nil then
            FirstDigit = number_words[candidate_string .. string.sub(s, i, i)]
            break
        end
        -- check if the next candidate_string is a possible prefix of a number words
        local next_candidate_string = candidate_string .. string.sub(s, i ,i)
        local found_candidate = false
        for ii=1,string.len(next_candidate_string) do
            test_string = string.sub(next_candidate_string, ii)
            for key, _ in pairs(number_words) do
                if string.sub(key, 1, #test_string) == test_string then
                    candidate_string = test_string
                    found_candidate = true
                    break
                end
            end
            if found_candidate then break end
            if string.len(test_string) == 1 then
                -- last iteration and we did not find a valid thing
                candidate_string = ""
            end
        end
    end
    if FirstDigit then
        for i=string.len(s),1,-1 do
            if digits[string.sub(s, i, i)] then
                return FirstDigit * 10 + string.sub(s, i, i)
            end
            if number_words[string.sub(s, i, i) .. candidate_string] ~= nil then
                return FirstDigit * 10 + number_words[string.sub(s, i, i) .. candidate_string]
            end
            local next_candidate_string = string.sub(s, i, i) .. candidate_string
            local found_candidate = false
            for ii=-1,-#next_candidate_string,-1 do
                test_string = string.sub(next_candidate_string, -#next_candidate_string, ii)
                for key, _ in pairs(number_words) do
                    if string.sub(key, -#test_string) == test_string then
                        candidate_string = test_string
                        found_candidate = true
                        break
                    end
                end
                if found_candidate then break end
                if #test_string == 1 then
                    candidate_string = ""
                end
            end
        end
    end
end

